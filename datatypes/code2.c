
//operations of two digits taking an user input 


#include <stdio.h>

int main() {
    int num1, num2; 

    printf("Enter the first digit: ");
    scanf("%d", &num1);

    printf("Enter the second digit: ");
    scanf("%d", &num2);

    printf("Addition: %d\n", num1 + num2);
    printf("Subtraction: %d\n", num1 - num2);
    printf("Multiplication: %d\n", num1 * num2);

}
